//*************************************************************************************************

package mas.game;

//.................................................................................................

import java.awt.Image;

//*************************************************************************************************

public class Area
{
	//..............................................................................................

	private int left = 0;
	private int top = 0;
	private int width = 0;
	private int height = 0;

	//..............................................................................................

	public Area ()
	{
	}

	//..............................................................................................

	public Area (Area _area)
	{
		setArea(_area);
	}

	//..............................................................................................

	public Area (int _left, int _top)
	{
		setPos(_left, _top);
	}

	//...............................................................................................

	public Area (int _left, int _top, int _width, int _height)
	{
		setArea(_left, _top, _width, _height);
	}

	//..............................................................................................

	public Area (int _left, int _top, Image _image)
	{
		if (_image != null)
			setArea(_left, _top, _image.getWidth(null), _image.getHeight(null));
		else
			setArea(_left, _top, 0, 0);
	}

	//...............................................................................................

	public int getLeft ()
	{
		return left;
	}

	//..............................................................................................

	public int getTop ()
	{
		return top;
	}

	//..............................................................................................

	public int getRight ()
	{
		return (left + width) - 1;
	}

	//..............................................................................................

	public int getBottom ()
	{
		return (top + height) - 1;
	}

	//..............................................................................................

	public int getWidth ()
	{
		return width;
	}

	//..............................................................................................

	public int getHeight ()
	{
		return height;
	}

	//..............................................................................................
/*
	public int getCenterX ()
	{
		return left + (width >> 1);
	}

	//...............................................................................................

	public int getCenterY ()
	{
		return top + (height >> 1);
	}
*/
	//...............................................................................................

	public void setLeft (int _left)
	{
		left = _left;
	}

	//..............................................................................................

	public void setTop (int _top)
	{
		top = _top;
	}

	//..............................................................................................

	public void setPos (int _left, int _top)
	{
		left = _left;
		top = _top;
	}

	//..............................................................................................

	public void setWidth (int _width)
	{
		width = _width;
	}

	//..............................................................................................

	public void setHeight (int _height)
	{
		height = _height;
	}

	//..............................................................................................

	public void setSize (int _width, int _height)
	{
		width = _width;
		height = _height;
	}

	//..............................................................................................

	public void addLeft (int _leftoffset)
	{
		left += _leftoffset;
	}

	//...............................................................................................

	public void addTop (int _topoffset)
	{
		top += _topoffset;
	}

	//...............................................................................................

	public void addPos (int _leftoffset, int _topoffset)
	{
		left += _leftoffset;
		top += _topoffset;
	}

	//...............................................................................................
/*
	public void setCenterX (int _xpos)
	{
		left = _xpos - (width >> 1);
	}

	//...............................................................................................

	public void setCenterY (int _ypos)
	{
		top = _ypos - (height >> 1);
	}

	//...............................................................................................

	public void setCenterPos (int _xpos, int _ypos)
	{
		left = _xpos - (width >> 1);
		top = _ypos - (height >> 1);
	}
*/
	//...............................................................................................

	public void setArea (int _left, int _top, int _width, int _height)
	{
		left = _left;
		top = _top;
		width = _width;
		height = _height;
	}

	//...............................................................................................

	public void setArea (Area _area)
	{
		left = _area.left;
		top = _area.top;
		width = _area.width;
		height = _area.height;
	}

	//...............................................................................................

	public boolean isContaint (int _xpos, int _ypos)
	{
		if (_xpos < left || _xpos >= (left + width) ||
			_ypos < top || _ypos >= (top + height))
			return false;

		return true;
	}

	//...............................................................................................
/*
	public boolean isCrossing (Area _area)
	{
		if (((_area.left + _area.width) <= left) || (_area.left  >= (left + width)) ||
		((_area.top + _area.height) <= top) || (_area.top  >= (top + height)))
			return false;

		return true;
	}
*/
	//...............................................................................................
}

