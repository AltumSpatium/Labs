package mas.game;

//.................................................................................................

import java.awt.Graphics;
import java.awt.Image;

//*************************************************************************************************

public class Picture extends Primitive
{
	//.............................................................................................

	private Image image = null;

	//.............................................................................................

	public Picture (int _type, String _id, String _resourcelink)
	{
		super(_type, _id);
		loadResource(_resourcelink);
	}

	//.............................................................................................

	public Picture (int _type, String _id, String _resourcelink, int _left, int _top)
	{
		super(_type, _id, _left, _top);
		loadResource(_resourcelink);
	}

	public void loadResource (String _resourcelink)
	{
		setSize(0, 0);

		if ((image = Utils.loadImage(_resourcelink)) != null)
			setSize(image.getWidth(null), image.getHeight(null));
	}

	//.............................................................................................

	public void setFocus (boolean _enabled)
	{		
	}

	//.............................................................................................

	public boolean isFocus ()
	{
		return false;
	}

	//.............................................................................................

	public boolean canFocus ()
	{
		return false;
	}

	//.............................................................................................

	public void setAnimate (boolean _animate, long _deltatime, boolean _useloop)
	{		
	}

	//.............................................................................................

	public boolean isAnimate ()
	{
		return false;
	}

	//.............................................................................................

	public boolean isPlaying ()
	{
		return false;
	}

	//.............................................................................................

	public boolean canAnimate ()
	{
		return false;
	}

	//.............................................................................................

	public void drawImage (Graphics _graphics)
	{
		if (isVisible() && image != null)
			_graphics.drawImage(image, getLeft(), getTop(), null);
	}

	//.............................................................................................

	public int getLayersCount ()
	{
		return 1;
	}

	//.............................................................................................

	public int getLayerIndex ()
	{
		return 0;
	}

	//.............................................................................................

	public void setLayerIndex (int _layerindex)
	{		
	}

	//.............................................................................................

	public int getImagesCount (int _layerindex)
	{
		if (_layerindex == 0)
			return 1;

		return 0;
	}

	//.............................................................................................

	public int getImageIndex ()
	{
		return 0;
	}

	//.............................................................................................

	public void setImageIndex (int _imageindex)
	{		
	}

	//.............................................................................................

	public void animateUpdate ()
	{		
	}

	//.............................................................................................

	public void animateNext ()
	{		
	}

	//.............................................................................................

	public void stateUpdate ()
	{		
	}

	//.............................................................................................
}

